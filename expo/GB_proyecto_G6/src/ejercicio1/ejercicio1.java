    int leerNumero() { // devuelve el numero ingresado
        return Integer.parseInt(txtNumero.getText());
    }
    
    public void ingresar() { //ingresa numero al arreglo
        num = leerNumero();
        contador = 0;
        numero = new int[1000];
        numero[contador++] = num;
        mostrar();
    }
    
    void mostrar() {// muestra el contenido que nos pide
       int temp[] = new int[contador++]; 
        
        for (int i = 0; i < temp.length; i++) {
            if (numero[i] % 2 == 0) {
                
                temp[i] = numero[i];
                txtaResultado.append("" + temp[i] + "\n");
            } else {
                impar++;
                txtImpar.setText("" + impar);
            }
        }
    }
    
    String validar() {//valida los numeros ingresado
        if (txtNumero.getText().equals("") || leerNumero() == 0) {
            return "ingrese numero";
        } else {
            return "";
        }
    }

